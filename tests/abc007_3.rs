extern crate assert_cli;

const NAME: &str = "abc007_3";

#[test]
fn test_0() {
    let stdin = r#"
7 8
2 2
4 5
########
#......#
#.######
#..#...#
#..##..#
##.....#
########
    "#;
    assert_cli::Assert::cargo_binary(NAME)
        .stdin(stdin)
        .stdout()
        .is("11")
        .unwrap();
}

